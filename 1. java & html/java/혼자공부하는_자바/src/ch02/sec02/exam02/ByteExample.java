public class ByteExample {

	public static void main(String[] args) {
		
		byte var0 = -129;	//컴파일에러
		byte var1 = -128;
		byte var2 = -30;
		byte var3 = 0;
		byte var4 = 30;
		byte var5 = 127;
		byte var6 = 128;	//컴파일에러
		//byte의 범위: -128~127

		System.out.println(var1);
		System.out.println(var2);
	}
}


		
